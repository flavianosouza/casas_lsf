#!/usr/bin/env node
/**
 * Auto-generates the APP_ROUTES allowlist used by middleware.ts
 * by scanning frontend/src/app/ for top-level pages.
 *
 * Run automatically via `prebuild` / `predev` npm scripts.
 *
 * Why: middleware redirects unknown root paths to /blog/<slug> to preserve
 * old WordPress URLs. Without this allowlist, every new static page would
 * 404 until manually added. This script keeps the allowlist in sync.
 */
import { readdirSync, statSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const APP_DIR = resolve(__dirname, "..", "src", "app");
const OUTPUT = resolve(__dirname, "..", "src", "lib", "app-routes.generated.ts");

// Folders that contain a page.tsx but should NOT be in APP_ROUTES
// because they have dynamic children handled separately by middleware
// SKIP_PREFIXES (e.g. /blog/, /portfolio/, /plantas/tipologia/).
// The ROOT index pages of these sections (e.g. /blog) DO belong in
// APP_ROUTES — only the children should be excluded.
const SECTION_INDEXES_KEEP = new Set(["blog", "plantas", "portfolio"]);

// Folders to ignore entirely (no route emitted)
const IGNORE = new Set(["api"]);

function listTopLevelRoutes() {
  const routes = new Set();

  // Root page.tsx → "/"
  if (existsSync(join(APP_DIR, "page.tsx"))) routes.add("/");

  for (const entry of readdirSync(APP_DIR)) {
    if (entry.startsWith("[") || entry.startsWith("_") || entry.startsWith(".")) continue;
    if (IGNORE.has(entry)) continue;

    const full = join(APP_DIR, entry);
    if (!statSync(full).isDirectory()) continue;

    if (existsSync(join(full, "page.tsx"))) {
      routes.add(`/${entry}`);
    }
  }
  return routes;
}

function emit(routes) {
  const sorted = [...routes].sort((a, b) => (a === "/" ? -1 : b === "/" ? 1 : a.localeCompare(b)));
  const body = sorted.map((r) => `  ${JSON.stringify(r)},`).join("\n");
  const banner = `// AUTO-GENERATED by scripts/generate-app-routes.mjs
// Do not edit by hand. Regenerate with: npm run gen:routes
// Source: scan of frontend/src/app/<dir>/page.tsx
`;
  const ts = `${banner}
export const APP_ROUTES: ReadonlySet<string> = new Set([
${body}
]);
`;
  writeFileSync(OUTPUT, ts, "utf8");
  console.log(`[gen:routes] ${sorted.length} routes → ${OUTPUT}`);
  for (const r of sorted) console.log(`  ${r}`);
  // Sanity: warn if section indexes are missing
  for (const s of SECTION_INDEXES_KEEP) {
    if (!sorted.includes(`/${s}`)) {
      console.warn(`[gen:routes] WARN: /${s} not found — confirm app/${s}/page.tsx exists`);
    }
  }
}

emit(listTopLevelRoutes());
