#!/usr/bin/env node
/**
 * Auto-generates the WP_BLOG_SLUGS allowlist used by middleware.ts.
 *
 * Why: middleware previously redirected ANY unknown root path to /blog/<slug>,
 * which created phantom 301→404 chains for typos/bots/scanners and polluted
 * Google's index. This list narrows the redirect to slugs that actually exist
 * (published WP articles + 19 renamed-slug aliases) so unknown URLs return
 * a clean 404 instead.
 *
 * Resilience:
 *   - On API failure with existing output → keep old snapshot (don't overwrite).
 *   - On API failure with no existing output → write empty Set + warn.
 *   - On API success → overwrite with fresh slug list.
 *
 * Run via `prebuild` / `predev` npm scripts.
 */
import { writeFileSync, existsSync, readFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT = resolve(
  __dirname,
  "..",
  "src",
  "lib",
  "blog-slugs.generated.ts",
);

const ENV_API =
  process.env.NEXT_PUBLIC_API_URL ||
  process.env.API_URL ||
  "https://casas-lsf-backend.dy3pb5.easypanel.host";

const ENDPOINT = `${ENV_API.replace(/\/$/, "")}/api/artigos/slugs`;

// Old WP slugs that were renamed (kept in sync with middleware.ts BLOG_SLUG_REDIRECTS)
// These must stay redirectable even though they no longer exist in the DB.
const RENAMED_OLD_SLUGS = [
  "telhados-e-coberturas-preco-m2-portugal",
  "casas-modulares-t3-portugal-precos",
  "manta-termica-telhado-preco-m2",
  "5-maneiras-de-otimizar-o-valor-do-metro-quadrado-de-construcao-em-2023",
  "descubra-as-10-melhores-empresas-de-construcao-lsf-em-2023",
  "iva-autoliquidacao-na-construcao-civil-em-2023-tudo-o-que-voce-precisa-saber",
  "preco-das-casas-em-portugal-com-queda-ate-44-em-2023",
  "curso-lsf-portugal-2023-tudo-o-que-precisa-saber-para-comecar",
  "the-average-cost-to-build-a-1500-sq-ft-house-in-2023",
  "quanto-custa-construir-uma-moradia-em-2023",
  "tabela-preco-construcao-civil-metro-quadrado-2023-portugal-guia-completo",
  "how-much-does-it-cost-to-build-a-house-in-2023",
  "vale-a-pena-vender-um-apartamento-para-construir-uma-casa-em-lsf-em-2023",
  "qual-e-o-melhor-metodo-construtivo-de-2023",
  "por-causa-das-chuvas-a-casa-ficou-inundada-precos-low-cost-2023",
  "incentivos-para-a-recuperacao-de-casas-antigas-2023",
];

function emit(slugs, source) {
  const sorted = [...new Set(slugs)].sort();
  const body = sorted.map((s) => `  ${JSON.stringify(s)},`).join("\n");
  const banner = `// AUTO-GENERATED by scripts/generate-blog-slugs.mjs
// Do not edit by hand. Regenerate with: npm run gen:slugs
// Source: ${source}
`;
  const ts = `${banner}
export const WP_BLOG_SLUGS: ReadonlySet<string> = new Set([
${body}
]);
`;
  writeFileSync(OUTPUT, ts, "utf8");
  console.log(`[gen:slugs] ${sorted.length} slugs → ${OUTPUT}`);
}

async function fetchSlugsWithTimeout(url, timeoutMs = 15000) {
  const controller = new AbortController();
  const t = setTimeout(() => controller.abort(), timeoutMs);
  try {
    const res = await fetch(url, { signal: controller.signal });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const data = await res.json();
    if (!Array.isArray(data)) throw new Error("Response is not an array");
    return data
      .map((row) => row?.slug)
      .filter((s) => typeof s === "string" && s.length > 0);
  } finally {
    clearTimeout(t);
  }
}

async function main() {
  let apiSlugs = [];
  let apiOk = false;
  try {
    apiSlugs = await fetchSlugsWithTimeout(ENDPOINT);
    apiOk = true;
    console.log(`[gen:slugs] fetched ${apiSlugs.length} from ${ENDPOINT}`);
  } catch (err) {
    console.warn(`[gen:slugs] API fetch failed: ${err.message}`);
  }

  if (apiOk) {
    emit([...apiSlugs, ...RENAMED_OLD_SLUGS], `API ${ENDPOINT} + renamed aliases`);
    return;
  }

  // API failed — preserve existing snapshot if present
  if (existsSync(OUTPUT)) {
    const existing = readFileSync(OUTPUT, "utf8");
    const matches = existing.match(/"([^"]+)"/g) || [];
    console.warn(
      `[gen:slugs] keeping existing snapshot at ${OUTPUT} (${matches.length} slugs). NOT overwriting.`,
    );
    return;
  }

  // No snapshot, no API — write minimal set with renamed aliases only
  console.warn(
    `[gen:slugs] no existing snapshot and API unreachable — writing fallback with ${RENAMED_OLD_SLUGS.length} renamed aliases only`,
  );
  emit(RENAMED_OLD_SLUGS, "fallback (renamed aliases only — API was unreachable)");
}

main().catch((e) => {
  console.error(`[gen:slugs] fatal:`, e);
  process.exit(1);
});
